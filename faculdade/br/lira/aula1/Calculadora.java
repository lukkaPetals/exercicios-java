package br.lira.aula1;

public class Calculadora {
    int soma(int num1, int num2) {
        int soma = num1 + num2;
        return soma;
    }
    int subtração(int x, int y) {
        int sub = x - y;
        return sub;
    }
    int multiplicacao(int x, int y) {
        int mult = x * y;
        return mult;
    }
    double divisao(double x, double y) {
        double div = x / y;
        return div;
    }
}
