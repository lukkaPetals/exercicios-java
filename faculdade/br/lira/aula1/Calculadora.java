package lira.aula1;

public class Calculadora {
    double soma(double num1, double num2) {
        double soma = num1 + num2;
        return soma;
    }

    double subtração(double x, double y) {
        double sub = x - y;
        return sub;
    }

    double multiplicacao(double x, double y) {
        double mult = x * y;
        return mult;
    }

    double divisao(double x, double y) {
        double div = x / y;
        return div;
    }
}
